function toolKey(value) {
  return String(value || "")
    .toLowerCase()
    .trim()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

const generatedRowDenyPatterns = [
  /leak|leaked|exposed|from-source|source-code/i,
];

function shouldSkipGeneratedTool(tool) {
  const text = [tool.id, tool.name, tool.description, tool.githubRepo, ...(tool.discovery?.topics || [])]
    .filter(Boolean)
    .join(" ");
  return generatedRowDenyPatterns.some((pattern) => pattern.test(text));
}

