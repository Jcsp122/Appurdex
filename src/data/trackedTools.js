function modelSupportForTool(tool) {
  const seeded = SOURCE_BACKED_MODEL_SUPPORT[tool.id];
  if (!seeded && !tool.modelSupport) return null;
  const current = tool.modelSupport || seeded;
  return {
    ...current,
    sourceUrl: current.sourceUrl || tool.sourceUrl || tool.website || "",
    sourceLabel: current.sourceLabel || tool.sourceLabel || "Official source",
    verifiedAt: current.verifiedAt || tool.lastCuratedAt || "",
  };
}

function applyVendorMetadata(tool) {
  const vendorId = tool.vendorId || vendorProductMap[tool.id];
  const vendor = vendorId ? vendorById[vendorId] : null;
  const modelSupport = modelSupportForTool(tool);
  const ecosystem = tool.ecosystem || (tool.category === "MCP server" ? "MCP Servers" : tool.category === "App builder" ? "AI App Builders" : "Agents");
  return {
    ...tool,
    ecosystem,
    ...(vendor ? {
      vendorId,
      vendorName: tool.vendorName || vendor.name,
      vendorWebsite: tool.vendorWebsite || vendor.website,
      vendorSourceUrl: tool.vendorSourceUrl || vendor.sourceUrl,
      vendorSourceLabel: tool.vendorSourceLabel || vendor.sourceLabel,
    } : {}),
    ...(modelSupport ? { modelSupport } : {}),
  };
}

